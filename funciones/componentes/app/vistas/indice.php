var sortDirections = []; // Keeps track of the sort direction for each column

function sortTable(columnIndex) {
    var table = document.getElementById("sortableTable");
    var rows = Array.from(table.rows).slice(1); // Get rows except for the header

    // Determine if the column is numeric or date
    var isNumericColumn = rows.every(row => !isNaN(row.cells[columnIndex].innerText));
    var isDateColumn = rows.every(row => !isNaN(Date.parse(row.cells[columnIndex].innerText)));

    // Initialize sort direction for the column if not already set
    if (!sortDirections[columnIndex]) {
        sortDirections[columnIndex] = 1;
    } else {
        sortDirections[columnIndex] *= -1; // Toggle sort direction
    }

    var sortedRows = rows.sort(function(a, b) {
        var x = a.cells[columnIndex].innerText;
        var y = b.cells[columnIndex].innerText;

        if (isNumericColumn) {
            return sortDirections[columnIndex] * (parseFloat(x) - parseFloat(y));
        } else if (isDateColumn) {
            return sortDirections[columnIndex] * (new Date(x) - new Date(y));
        } else {
            return sortDirections[columnIndex] * x.localeCompare(y);
        }
    });

    // Remove existing rows and append sorted rows
    var tbody = table.tBodies[0];
    tbody.innerHTML = "";
    sortedRows.forEach(function(row) {
        tbody.appendChild(row);
    });
}

